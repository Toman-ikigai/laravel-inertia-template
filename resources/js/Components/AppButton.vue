<template>
    <button :class="classes" @click="$emit('activated', true)">
        <span class="btn__text" v-text="text"></span>
    </button>
</template>

<script>
    export default {
        emits: ["activated"],

        props: {
            text: {
                type: String,
                default: "Submit",
            },
            type: String,
        },

        data() {
            return {};
        },

        computed: {
            classes() {
                let classes = ["btn"];

                if (this.type) {
                    classes.push(`btn--${this.type}`);
                }

                return classes.join(" ");
            },
        },
    };
</script>

<style lang="scss">
    .btn {
        display: inline-flex;
        justify-content: center;
        cursor: pointer;
        border-radius: border-radius(buttons);
        background-color: $ui-color-1;
        // Type
        font-family: font(1);
        font-weight: bold;
        text-align: center;
        color: #fff;
    }

    .btn--full {
        width: 100%;
    }

    .btn[disabled] {
        opacity: 0.5;
        cursor: not-allowed;
    }

