<template>
    <Head :title="title" />

    <h1
        v-text="title"
        class="d-mb-40 m-mb-30 text-center"
    ></h1>

    <div class="boxed">
        <form
            class="form"
            @submit.prevent="submitRegisterForm"
        >
            <div class="form__section">
                <div class="form__row">
                    <div class="form__item">
                        <label
                            class="form__label"
                            for="first-name"
                        >
                            First Name
                        </label>
                        <input
                            id="first-name"
                            type="text"
                            required
                            v-model="registerForm.first_name"
                        />
                    </div>
                </div>

                <div class="form__row">
                    <div class="form__item">
                        <label
                            class="form__label"
                            for="last-name"
                        >
                            Last Name
                        </label>
                        <input
                            id="last-name"
                            type="text"
                            required
                            v-model="registerForm.last_name"
                        />
                    </div>
                </div>

                <div class="form__row">
                    <div class="form__item">
                        <label
                            class="form__label"
                            for="email"
                        >
                            Email
                        </label>
                        <input
                            id="email"
                            type="email"
                            required
                            v-model="registerForm.email"
                        />
                    </div>
                </div>

                <div class="form__row">
                    <div class="form__item">
                        <label
                            class="form__label"
                            for="password"
                        >
                            Password
                        </label>
                        <input
                            id="password"
                            type="password"
                            required
                            v-model="registerForm.password"
                        />
                    </div>
                </div>

                <div class="form__row">
                    <div class="form__item">
                        <label
                            class="form__label"
                            for="org-name"
                        >
                            Organisation Name
                        </label>
                        <input
                            id="org-name"
                            type="text"
                            required
                            v-model="registerForm.organisation_name"
                        />
                    </div>
                </div>

                <div class="form__row">
                    <div class="form__action">
                        <Button
                            text="Register"
                            styles="full"
                            :disabled="registerForm.processing"
                        />
                    </div>
                </div>
            </div>
        </form>

        <div class="d-mt-30 m-mt-15 text-center">
            <p>
                <Link :href="route('login')">Login</Link>
            </p>
        </div>
    </div>
</template>

<script>
    import { useForm } from "@inertiajs/vue3";

    import GuestLayout from "@js/Layouts/Guest.vue";

    export default {
        layout: GuestLayout,

        props: {
            first_name: String,
            last_name: String,
            email: String,
            password: String,
            organisation_name: String,
        },

        data() {
            return {
                title: "Register",
                registerForm: useForm({
                    first_name: this.first_name,
                    last_name: this.last_name,
                    email: this.email,
                    password: this.password,
                    organisation_name: this.organisation_name,
                }),
            };
        },

        methods: {
            submitRegisterForm() {
                this.registerForm.post(route("register.store"));
            },
        },
    };
</script>
